public class SimulatedVehicle {
    private double saveTime;
    private double simulationTime;
    private double handleDegree;
    private double gasPedal;
    private double breakPedal;
    private double clutchPedal;
    private double speed;
    private double rpm;
    private double accelerationX;
    private double accelerationY;
    private double accelerationZ;
    private double positionX;
    private double positionY;
    private double positionZ;

    public SimulatedVehicle() {
    }

    public double getSaveTime() {
        return saveTime;
    }

    public void setSaveTime(double saveTime) {
        this.saveTime = saveTime;
    }

    public double getSimulationTime() {
        return simulationTime;
    }

    public void setSimulationTime(double simulationTime) {
        this.simulationTime = simulationTime;
    }

    public double getHandleDegree() {
        return handleDegree;
    }

    public void setHandleDegree(double handleDegree) {
        this.handleDegree = handleDegree;
    }

    public double getGasPedal() {
        return gasPedal;
    }

    public void setGasPedal(double gasPedal) {
        this.gasPedal = gasPedal;
    }

    public double getBreakPedal() {
        return breakPedal;
    }

    public void setBreakPedal(double breakPedal) {
        this.breakPedal = breakPedal;
    }

    public double getClutchPedal() {
        return clutchPedal;
    }

    public void setClutchPedal(double clutchPedal) {
        this.clutchPedal = clutchPedal;
    }

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public double getRpm() {
        return rpm;
    }

    public void setRpm(double rpm) {
        this.rpm = rpm;
    }

    public double getAccelerationX() {
        return accelerationX;
    }

    public void setAccelerationX(double accelerationX) {
        this.accelerationX = accelerationX;
    }

    public double getAccelerationY() {
        return accelerationY;
    }

    public void setAccelerationY(double accelerationY) {
        this.accelerationY = accelerationY;
    }

    public double getAccelerationZ() {
        return accelerationZ;
    }

    public void setAccelerationZ(double accelerationZ) {
        this.accelerationZ = accelerationZ;
    }

    public double getPositionX() {
        return positionX;
    }

    public void setPositionX(double positionX) {
        this.positionX = positionX;
    }

    public double getPositionY() {
        return positionY;
    }

    public void setPositionY(double positionY) {
        this.positionY = positionY;
    }

    public double getPositionZ() {
        return positionZ;
    }

    public void setPositionZ(double positionZ) {
        this.positionZ = positionZ;
    }
}
